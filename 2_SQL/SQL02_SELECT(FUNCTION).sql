SELECT STUDENT_NO AS 학번, STUDENT_NAME AS 이름, ENTRANCE_DATE AS 입학년도
FROM TB_STUDENT
WHERE DEPARTMENT_NO = '002'
ORDER BY ENTRANCE_DATE;

--2.
SELECT PROFESSOR_NAME, PROFESSOR_SSN
FROM TB_PROFESSOR
WHERE PROFESSOR_NAME NOT LIKE '___';

--3.
SELECT PROFESSOR_NAME AS 교수이름, 
(EXTRACT(YEAR FROM SYSDATE) 
-EXTRACT(YEAR FROM (TO_DATE (SUBSTR (PROFESSOR_SSN,1,6), 'YYMMDD'))))
+100 AS 나이
FROM TB_PROFESSOR
ORDER BY 나이;

--4.
SELECT SUBSTR(PROFESSOR_NAME,2,2) AS 이름
FROM TB_PROFESSOR;

--5.
SELECT *
FROM (
    SELECT STUDENT_NO, STUDENT_NAME, 
           ABS(EXTRACT(YEAR FROM ENTRANCE_DATE) - 
               (CASE
                    WHEN SUBSTR(STUDENT_SSN, 1, 2) > TO_CHAR(SYSDATE, 'YY')
                    THEN 1900 + TO_NUMBER(SUBSTR(STUDENT_SSN, 1, 2))
                    ELSE 2000 + TO_NUMBER(SUBSTR(STUDENT_SSN, 1, 2))
               END)) AS 재수
    FROM TB_STUDENT
) 
WHERE 재수 > 19;

